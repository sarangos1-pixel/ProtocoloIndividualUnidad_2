public class Utilidades {
   public static int suma(int a, int b) {
        return a + b;
    }
   
   public static int resta(int a, int b) {
        return a - b;
    }
   
   public static int mult(int a, int b){
       return a * b;
   }
   
   public static double div(double a, double b){
       return a / b;
   }
   
    
}
